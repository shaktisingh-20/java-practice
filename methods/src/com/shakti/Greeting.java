public class Greeting {
    public static void main(String[] args) {
        String name = "rahul bhai";
        greet(name);
        System.out.println(name);
    }

    static String greet(String name) {
        String message = "hello";
        name = "kalluuuu";
        System.out.println(message+" "+ name);
        return message;
    };
};